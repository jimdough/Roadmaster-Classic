	  	  <h1>What does it take to be a professional truck driver?</h1>
	  	  <p>Ambition, motivation, and the desire to be successful!  For those who have what it takes, truck driving can provide job security, benefits, and great earning potential; not to mention, flexibility and advancement that other occupations do not. Most truck drivers are long-haul or over-the-road drivers, operating trucks with a capacity of at least 26,001 pounds, transporting goods from one location to another. After a little experience over the road, many drivers become company trainers, safety supervisors, dispatchers, and more which may offer increased pay levels and more time at home. For those who have the determination and entrepreneurial spirit, becoming an Owner/Operator and owning your own business is a great option! Sounds good, right?</p>
	  	  
	  	  <p>To drive professionally, you must obtain a Class A CDL License. Most major trucking companies seek entry level drivers that have received professional truck driver training.  CDL training from a well-known truck driving school gives you comprehensive instruction and the skill set needed from instructors who have years of real-world experience.</p>
	  	  
	  	  <p>Additional responsibilities include maintaining accurate records and logs of your routes, pre and post-trip inspections ensuring safe operation, proper loading and securing of freight.   If driving for a trucking company, you will work closely with your dispatcher to plan trips so you meet pick-up and delivery schedules.</p>

	  	  
