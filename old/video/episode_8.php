 <h2>How do you become a certified CDL Professional?</h2>
 
 <p>The CDL exam is setup in two parts. The first part is the knowledge portion where you have to have the knowledge of; general knowledge of operating the vehicle and other knowledge such as air brake systems and how they work, combination trucks and how they go together and so on. Hazardous material, tank; a certain amount of tank knowledge if you're going to be a tank truck driver, and all of those what we call endorsement tests would have to be passed for the first part; and most states require endorsement tests to be passed in order to issue a permit or CDL license, and then of course there's a skills portion that also has 3 parts:</p>

<p>Equipment inspection, skills backing training, and the skills over the road training which you actually have to perform in front of a CDL examiner.</p>

