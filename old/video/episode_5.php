 <h2>What health conditions could possibly keep you from getting a CDL license?</h2>
 
 <p>The federal regulations mandate the health regulations for drivers. You must have what they call a DOT physical exam every 2 years. In order to pass that exam you would have to meet the federal requirements for doing so.</p>

<p>What conditions might prevent you from passing the physical exam?</p>

<p>Some things that would keep you from passing that exam might be bad eyesight, I think 20/40 vision in both eyes is the requirement and in each eye, so if you're blind in one eye and 20/20 in the other, you would still would not meet the federal regulation to pass the physical. Other things such as diabetes and so on would preclude you from passing the DOT physical.
</p>

