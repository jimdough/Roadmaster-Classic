 <h2>What is involved in a CDL License background check?</h2>
 
 <p>There is a national database called CDLIS that records all of the CDL driver licenses in the country and all of the information around that CDL license is documented in that database. So as far as background checks, most prospective students would go from a regular drivers license to a CDL license and do it through a CDL permit and they would normally check that drivers license record, not necessarily move that data in to the commercial driver license database but at least see whether there were a lot of moving violations or whether there were DUIs.</p>

