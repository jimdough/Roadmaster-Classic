 <h1>Truck Driving School requirements are an important consideration when deciding to attend cdl a training. The requirements below are some of the more common requirements a student will have to meet before attending their first cdl class</h1>
 
<img src="/images/cdl-class.jpg" alt="CDL Class" align="right"><h3>CDL Class Pre-Enrollment Requirements</h3>
<ul>
<li>You must be Eighteen (18) years of age to drive intrastate or at least twenty-one (21) years old to be employed to drive interstate.</li>
<li>Beyond the age of compulsory high school attendance as defined by each state.</li>
<li>Possess a valid driver license at the time of enrollment and one (1) year of automobile driving experience  (Must maintain a valid driver license during the CDL A training period).</li>
<li>Provide Social Security Card or Valid Picture ID.</li>
<li>Drug free and agree to random drug testing during <b>CDL A training</b></li>
<li>Ability to read, speak, and understand English language sufficiently to converse with general public, to understand highway traffic signs and signals, to respond to official inquiries and to make legible entries on reports and records.</li>
<li>No language services are provided. Instruction is provided in English only.</li>
<li>Special Entrance Requirements for Texas, Utah, Arkansas and Tennessee.*</li></ul>


<h3>CDL Class Graduation Requirements</h3>
<ul>
  <li> You must complete all of the pre-determined hours of CDL A training.</li>
  <li>Achieve at least a 70% overall grade average.
    <ul>
      <li>When a student does not meet specific standards for a phase of training, additional instruction may<br />
        be required and given.</li>
      <li>If additional time is required to achieve an approved level of performance, there will be no extra<br />
        charge to the student.</li>
      <li>Amount of time spent on any field-training topic may vary among students.</li>
    </ul>
  </li>
</ul>

<h3>Certificate of Completion</h3>
<p>Upon graduation, you will receive a Certificate of Completion and assistance in obtaining your <a href="cdl_training.php">Commercial Driver License</a> (CDL license)</p>
<ul>
  <li>In <a href="truck_school_SANANTONIO.php">Texas</a>, Students completing the program requirements will receive a Compliance and Completion Certificate</li>
  <li>In <a href="truck_school_OHIO.php">Ohio</a>, Students completing the program requirements will receive an Ohio Certificate of Completion</li>
</ul></p>


<h3>Attendance Policy Highlights</h3>
<ul>
  <li>Attendance at each scheduled <b>cdl class</b> is required and recorded.</li>
  <li>Students are required to report to class on time.</li>
  <li>Absences will be recognized as valid in the event of student illness or death of an immediate family member.</li>
  <li>When an absence is authorized, the student will be entitled to make-up classes at no additional cost, although this does not remove the absence.</li>
  <li>All missed classes must be made up prior to graduation.</li>
  <li>It is the responsibility of the student to schedule make-up time with the Director of Training.</li>
  <li>Each unexcused absence will require the student to repeat the scheduled class related to the absence / tardiness.</li>
  <li>Students are required to notify the Director of Training when they know they will be absent or tardy.</li>
</ul>

<h3>Incomplete Policy</h3>
<ul>
  <li>Any student not completing the CDL A training within the prescribed time frame is eligible for a refund of tuition based on the applicable state refund policy.</li>
  <li>A student who withdraws or is withdrawn from Roadmaster <b>truck driving school</b> is eligible for a refund of tuition based on the applicable state refund policy.</li>
</ul>

<h3>Veteran Policies</h3>
<p> <b>Roadmaster</b> is also Approved for Veterans Training. Student eligibility for Veterans Education Benefits is established by the Veterans Administration. Initial eligibility and continued eligibility during enrollment is determined by the VA. The following policies are applicable to students receiving VA Benefits:</p>

<img src="/images/truck-driving-school.jpg" alt="Truck Driving School" align="right"><h3>Veterans' Attendance Policy Highlights</h3>
<p>VA approved students must adhere to the same attendance policy as other students attending <b>Roadmaster</b> truck driving school. In addition to the attendance policy stated in the official catalog, students receiving VA benefits in order to attend Roadmaster Drivers School must also adhere to the following:</p>
<ul>
  <li>Excused absences will be granted for extenuating circumstances only and must be substantiated by entries on the student’s academic file</li>
  <li>Legitimate documentation of why the absence occurred must be turned in to the Director of Training within a week of the absence</li>
  <li>Early departures, class cuts, and tardies for any portion of an hour will be counted as a full hour of absence</li>
</ul>

<h3>Standards of Satisfactory Progress for VA Approved Students</h3>
<p>Students receiving VA benefits are required to maintain satisfactory progress to remain in good academic standing. Satisfactory Academic Progress for VA students is defined as follows:</p>

<ul>
  <li>A veteran must maintain an overall grade point average of at least a C, seventy (70%), at the end of each grading period</li>
  <li>If the cumulative grade point average falls below a C, seventy (70%), the student will be placed on probation for the next evaluation period </li>
  <li>If the grade point average remains below 70% at the end of the next evaluation period, the VA will be notified of the probation, thus leading to the termination of the benefits</li>
  <li>VA students may request reinstatement after a full term of dismissal has passed</li>
</ul>

