<h1>What is <b>truck driving school</b> really like?</h1>

<p>Roadmaster is a hands-on truck-driving school and CDL training center with
locations across the United States. Roadmaster provides you with the CDL
training you need to get either a Class A or Class B CDL license to help
you start your professional trucking career.</p>

<p>With classes forming weekly, you can be on your way to an exciting new
career in as little as three to four weeks. Roadmaster truck driving school
offers free job placement assistance after graduation. Tuition assistance
is also available for those who qualify.</p>

<p>Roadmaster Drivers School provides professional truck driver training that
will prepare you for a career in the trucking industry. And how do we do
that?</p>

<p>You're going to get intense training. You're going to get complete training
for the trucking industry. We cover all aspects of everything that's needed
for the trucking industry. So when you leave here, you're fully prepared to
go out and start with a company. And what we do here is we build a good
foundation of a student that's never even been inside of a truck before.
Start with classroom. We cover all the classroom curriculum, the logs, the
recap. We cover federal motor carriers regulations. Different things inside
the classroom, we prepare them for the permits.</p>

<p>Half the class will be out on a back-up pad. The other half of the class
will be on the road. So all the new guys will be in the classroom for the
first, pretty much the first 40 hours of their training is in the
classroom. But they're not always in the classroom. They're outside doing
pre-trip inspection training. They're doing logbook training. We have a Pro-
TREAD lab, a computer lab. They do some audio-visual interactive computer
stuff. They do that. So there's a lot of stuff going on in that first 40
hours of school.</p>

<p>You're going to be in a real truck. We have a 22-foot conventionals. We
have 48- and 53-foot trailers. And by the end of the first week, they're
actually driving, shifting, up-shifting, down-shifting, and driving tractor
trailers. They go out, they drive on the highway each day, progressively
getting more complicated, until the point that they're able to pass those
tests at the end of the three weeks on their own.</p>

<p>Still wondering if a new career as a CDL professional is right for you?
People from all walks of life have found their exciting new career at

<p>There's all sorts of people that come in to see me. I've got people that
have master's degrees. I have people that didn't graduate high school. We
get everybody here. It's the most diverse community you will ever see is at
Roadmaster. I just had a guy in here, and he was in construction. Now a lot
of my students are former construction people that worked in that industry.
Well construction, as you know, is up and down. So he would tell me, "Well,
I'd worked three months, and I'd go on unemployment. Work another three
months and go on unemployment." And a lot of why he was here, hey I don't
think he just woke up one day and said "I want to be a truck driver." I
think he was looking for something that was good, consistent income that he
can support his family with. And I think that's what brought him to
Roadmaster.</p>

<p>In these uncertain economic times, Roadmaster provides you with the
training you need to begin a stable career for you and your family.</p>

<p>There is no such thing as a laid-off truck driver. Like I said, we have an
excellent placement rate. Roadmaster in general, across the board, has an
excellent placement rate. If they can get through the representative and
get into school, we can usually work with anybody, and as long as they're
willing to work with us and do the applications, they're going to go to
work. They're going to go to work.</p>

<p>Everything that's delivered in America, everything is delivered by truck.
There are four million truck drivers in America. There is work. I have
jobs.</p>

<p>The majority of all of our students have jobs waiting for them before they
graduate. Some have more than one.</p>

<p>Roadmaster Drivers School features a short three to four week CDL training
program. Hands-on truck driver training. Classes available days, evenings,
and weekends with new training sessions forming weekly. Tuition assistance
available for those who qualify. Job placement assistance available after
graduation. For those who are qualified, you can even be pre-hired before
you finish training.</p>

