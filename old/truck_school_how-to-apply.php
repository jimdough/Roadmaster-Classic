 <h2>Main Application Requirements</h2>
 <p>Each Roadmaster Drivers School location has its own admissions office and staff to help students step by step with the application process. Admissions requirements may vary between location, but below are the main pre-requisites.</p>
  <img src="images/started.jpg" border="0" align="left" alt="Student attending cdl training classes" style="padding-top:1px; padding-right: 30px; padding-left:18px;" />

<ul>
<li>The applicant must be at least 18 years old.</li>
<li>If desiring a Class A CDL License for interstate driving employment, the applicant must be at least 21 years old.</li>
<li>The applicant must possess the ability to read, speak, and understand english language sufficiently to converse with general public, to understand highway traffic signs and signals, to respond to official inquiries, and to make legible entries on reports and records</li>
<li>Any applicant wishing to apply to one of Roadmaster's <b>truck driving schools</b> to take cdl training classes must have a valid drivers license in their possession at the time of enrollment.</li>
