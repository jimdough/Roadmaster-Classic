<p><b>Dear Roadmaster Staff Members,</b></p>

<p>Thank you! Our Roadmaster experience was a positive one. The entire staff seemed genuinely happy to assist students and truly concerned with our progress. You feel at home from the time you enter the building and meet either of the two (2) beautiful young women who operate the Roadmaster front desk. Even the maintenance person makes your day with a smile, not to mention his meticulous cleaning and upkeep of the property.</p>

<p>Edith Nieves helped to start our journey. Her willingness to answer questions and her ability to outline the program, immediately removed the fear of Roadmaster just being another fly-by-night, get-your-money-and-run set up. You instantly feel as though you can trust her. Thanks Edith!</p>

<p>Dawn's professional and easy going manner made signing away our lives almost painless. There was no doubt that she wanted to help us get our financial "stuff" in order so that we could complete the program and graduate. Thank you Dawn!</p>

<p>Dan was a great instructor. His ability to interject humor and life experience into his lessons, made what could have been frustrating and overwhelming, a bit clearer and easier to digest. Thanks Dan, for helping us pass our CDL permit test with flying colors!</p>

<p>Moving into week two was very stressful. Those trucks were huge! But our yard instructors moved forward as though it were perfectly normal to drive a building. They also shared their experiences and took the time to nurture those of us who were intimidated by the mere size and "backwards" right and left of the trucks. All of the yard instructors were great, but Mark's smooth and easy-going manner (and his Trucker's Dance) and Moses' almost fatherly approach to teaching made the difference for me. Hansel thought everyone was cool.</p>

<p>This program was amazing!! It almost seemed impossible to learn so much in such a short time! It is obvious that the great leadership at Roadmaster (Orlando) makes a difference. Despite "technical difficulties", students' personal crisis' and even extreme weather, Ron held it all together with the help of his phenomenal supporting cast... Doug (less), Doug (more), Jose, Ray Pepper, Bucci, Al, George, Jim, the sweet guy I stressed out by failing my P.T.I., Felix, Ray and all the guys in blue. Thanks guys!</p>

<p>Needless to say we made it through week three!! We have jobs, thanks to the wonderful efforts of Delores. Her guidance was priceless. She has a great rapport with the trucking companies. Her connections seem to expedite the process. Thank you Delores!</p>

<p>In conclusion, we would recommend the Roadmaster Drivers' Program to anyone looking to improve their future! Without a doubt, Roadmaster changes lives (by empowering people with education), impacts communities (by helping to employ people), and ultimately leaves its print on this great country... one load at a time!</p>

<p><b>Keep on trucking &amp; be blessed,</b></p>

<p><b>Hansel &amp; Veronj H.</b></p>

