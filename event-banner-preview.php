     <h3>Commercial Driver Regulations</h3>
     <p>Drivers of Commercial Motor Vehicles must adhere to  the hours of service, which are regulations governing the driving hours of commercial drivers. These, and all other rules regarding the safety of interstate commercial driving, are issued by the <a href="http://en.wikipedia.org/wiki/Federal_Motor_Carrier_Safety_Administration" title="Federal Motor Carrier Safety Administration" target="_blank">Federal Motor Carrier Safety Administration</a> (FMCSA). The FMCSA is also a division of the <a href="http://en.wikipedia.org/wiki/United_States_Department_of_Transportation" title="United States Department of Transportation" target="_blank">United States Department of Transportation</a> (USDOT), which governs all transportation-related industries such as trucking, shipping, railroads, and airlines. Some other issues are handled by another branch of the USDOT, the <a href="http://en.wikipedia.org/wiki/Federal_Highway_Administration" title="Federal Highway Administration" target="_blank">Federal Highway Administration</a> (FHWA).</p>
         
    <h3>How do I get my Commercial Drivers License?</h3>
 <p>States are able to issue a CDL License only after a written and driving test have been given by the state or approved testing facility. The minimum age to apply for a CDL license is usually 21, as required by the <a href="http://en.wikipedia.org/wiki/United_States_Department_of_Transportation" title="United States Department of Transportation" target="_blank">US Department of Transportation</a>,  although some states may allow drivers who are 18-20 to apply for a CDL  that is valid only within the Drivers state of residence. A single state CDL license only restricts driving of CMVs within the holder's state (not non-commercial vehicles), and automatically converts to a 50 state CDL license at the age of 21.</p>
 
 <h3>What type of CDL License should you train for?</h3>
 <p> Since April of 1992, all drivers have been required to have a CDL in order to drive a Commercial Motor Vehicle. Tractor-trailers require advanced skills and knowledge above and beyond those required to drive a car or other light weight vehicle.</p>
 <ol>
   <li><b>Class A CDL License:</b> is needed to operate any combination of vehicles with a <a href="http://en.wikipedia.org/wiki/GVWR" target="_blank">GVWR</a> of 26,001 or more pounds provided the <a href="http://en.wikipedia.org/wiki/GVWR" target="_blank">GVWR</a> of the vehicle(s) being towed is in excess of 10,000 pounds.
     <ul>
       <li>Vehicles requiring a Class A CDL license are primarily tractor-trailers.</li>
     </ul></li>

      <li><b>Class B CDL License:</b> is needed to operate  any single vehicle with a <a href="http://en.wikipedia.org/wiki/GVWR" target="_blank">GVWR</a> of 26,001 or less pounds or any such vehicle towing a vehicle not in excess of 10,000 pounds. Vehicles requiring a Class B CDL license may include:</li>
     
     <ul>
       <li>Cars with trailers, buses (regardless of size), passenger vans and motorhomes
       <li>Standard-sized dump trucks, tow trucks, cement mixers and garbage trucks
       <li>Delivery trucks and utility trucks</li>
       <li>Or any single vehicle with GVWR of 26,001 lbs. or more, and vehicle is not towing an excess of 10,000 lbs. GVWR</li>
    
       </ul>
   </li></ol>
   



